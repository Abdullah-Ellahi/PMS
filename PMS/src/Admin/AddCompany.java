    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String company = companynametxt.getText().toUpperCase();

        Connection con, c;
        PreparedStatement stm;
        Statement st;
        int a = 0;

        try {
            c = ConnectionProvider.getcon();
            st = c.createStatement();
            ResultSet rs = st.executeQuery("Select * From companies");

            while (rs.next()) {
                if (rs.getString("company_name").equals(company)) {
                    a = 1;
                }
            }

            c.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        if (company.equals("")) {
            JOptionPane.showMessageDialog(null, "Company name cannot be empty!");
        } else if (CheckName == 1) {
            JOptionPane.showMessageDialog(null, "Company name already exists!\nPlease enter another Company name.");
        } else if (a == 1) {
            JOptionPane.showMessageDialog(null, "Company name already exists!\nPlease enter another Company name.");
        } else {

            try {
                con = ConnectionProvider.getcon();

                stm = con.prepareStatement("insert into companies (company_name) values(?)");

                stm.setString(1, company);

                stm.executeUpdate();

                JOptionPane.showMessageDialog(null, "Company added successfully!");

                jTable1ComponentShown();
                jButton17ActionPerformed(evt);

            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

