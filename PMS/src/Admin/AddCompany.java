    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();

        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Please select a record to update");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String Oname = (String) model.getValueAt(index, 1);
        String Nname = companynametxt.getText();

        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Please select a record to edit!");
        } else {
            String Ocompany_name = (String) model.getValueAt(index, 1);

            String Ncompany_name = companynametxt.getText().toUpperCase();

            if (Ncompany_name.equals("")) {
                JOptionPane.showMessageDialog(null, "Company name cannot be empty!");
            } else if (CheckName == 1 && !Oname.equals(Nname)) {
                JOptionPane.showMessageDialog(null, "Company name already exists!\nPlease enter another name");
            } else {

                Connection con;
                PreparedStatement stm;

                try {
                    con = ConnectionProvider.getcon();
                    stm = con.prepareStatement("UPDATE `pharmacy`.`companies` SET `company_name` = ? WHERE (`company_name` = ?);");

                    stm.setString(1, Ncompany_name);
                    stm.setString(2, Ocompany_name);

                    stm.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Changes Saved successfully!");

                    //refreshing the table
                    jTable1ComponentShown();
                    jButton17ActionPerformed(evt);

                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

