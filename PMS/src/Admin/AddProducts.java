    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String name = nametxt.getText();
        String formula = formulatxt.getText();
        String type = (String) typecombotxt.getSelectedItem();
        String company = (String) distributorcombotxt.getSelectedItem();
        String packs = packstxt.getText();
        String loose = loosetxt.getText();
        String quantity_in_pack = quantityinpacktxt.getText();
        String price_of_pack = priceofpacktxt.getText();
        String wprice_of_pack = wpriceofpacktxt.getText();
        String discount_rate = discountratetxt.getText();
        String minimum_quantity = minimumquantitytxt.getText();

        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Name cannot be empty!");
        } else if (formula.equals("")) {
            JOptionPane.showMessageDialog(null, "Formula cannot be empty!");
        } else if (packs.equals("")) {
            JOptionPane.showMessageDialog(null, "'packs' field cannot be empty!");
        } else if (!packs.matches(Integerpattern) && !packs.equals("0")) {
            JOptionPane.showMessageDialog(null, "Invalid pattern!\n'Packs/Boxes' field should contain integers only.");
        } else if (loose.equals("")) {
            JOptionPane.showMessageDialog(null, "'loose' field can not be empty!");
        } else if (!loose.matches(Integerpattern) && !loose.equals("0")) {
            JOptionPane.showMessageDialog(null, "Invalid pattern!\n'Loose' field should contain integers only.");
        } else if (quantity_in_pack.equals("")) {
            JOptionPane.showMessageDialog(null, "'Quantity In Pack' field cannot be empty!");
        } else if (!quantity_in_pack.matches(Integerpattern)) {
            JOptionPane.showMessageDialog(null, "'Quantity In Pack' field should contain 'Integers' only!");
        } else if (quantity_in_pack.equals("0")) {
            JOptionPane.showMessageDialog(null, "'Quantity In Pack' can not be zero!");
        } else if (price_of_pack.equals("")) {
            JOptionPane.showMessageDialog(null, "'Price Of Pack' cannot be empty!");
        } else if (price_of_pack.equals("0")) {
            JOptionPane.showMessageDialog(null, "'Price Of Pack' can not be zero!");
        } else if (!price_of_pack.matches(decimalpattern) && !price_of_pack.matches(Integerpattern)) {
            JOptionPane.showMessageDialog(null, "Invlaid pattern!\n'Price Of Pack' field should contain integers or decimal values.");
        } else if (wprice_of_pack.equals("")) {
            JOptionPane.showMessageDialog(null, "'Wholesale Price Of Pack' cannot be empty!");
        } else if (!wprice_of_pack.matches(decimalpattern) && !wprice_of_pack.matches(Integerpattern)) {
            JOptionPane.showMessageDialog(null, "Invalid pattern!\n'Wholesale Price Of Pack' field should contain integers or decimal values.");
        } else if (discount_rate.equals("")) {
            JOptionPane.showMessageDialog(null, "'Discount Rate' cannot be empty!");
        } else if (!discount_rate.matches(decimalpattern) && !discount_rate.matches(Integerpattern)) {
            JOptionPane.showMessageDialog(null, "Invalid 'Discount Rate' pattern!\n'Discount Rate' field should contain integers or decimal values.");
        } else if (minimum_quantity.equals("")) {
            JOptionPane.showMessageDialog(null, "'Minimum Quantity' cannot be empty!");
        } else if (!minimum_quantity.matches(Integerpattern)) {
            JOptionPane.showMessageDialog(null, "Invalid pattern!\n'Minimum Quantity' field should contain integers only.");
        } else if (CheckName == 1) {
            JOptionPane.showMessageDialog(null, "Username already exists!\nPlease enter another Username.");
        } else {

            Connection con;
            PreparedStatement stm;

            try {
                con = ConnectionProvider.getcon();
                stm = con.prepareStatement("insert into products (name,formula,type,company,packs,loose,quantity_in_packs,price_of_pack,wprice_of_pack,price_of_loose,discount_rate,minimum_quantity) values(?,?,?,?,?,?,?,?,?,?,?,?)");

                stm.setString(1, name);
                stm.setString(2, formula);
                stm.setString(3, type);
                stm.setString(4, company);
                stm.setString(5, packs);
                stm.setString(6, loose);
                stm.setString(7, quantity_in_pack);
                stm.setString(8, price_of_pack);
                stm.setString(9, wprice_of_pack);

                Float price_of_loose = Float.valueOf(price_of_pack) / Float.valueOf(quantity_in_pack);
                stm.setString(10, price_of_loose.toString());

                stm.setString(11, discount_rate);
                stm.setString(12, minimum_quantity);

                stm.executeUpdate();

                JOptionPane.showMessageDialog(null, "Product added successfully!");
                jButton17ActionPerformed(evt);

            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

