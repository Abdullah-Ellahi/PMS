    private void nametxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nametxtKeyReleased
        // TODO add your handling code here:
        String name = nametxt.getText();

        if (!name.equals("")) {
            Iconlabel1.setVisible(true);
            Iconlabel1.setIcon(new ImageIcon("src\\images\\yes.png"));
            Iconlabel1.setText("");
            CheckName = 0;

            Connection myConn = null;
            Statement myStmt = null;
            ResultSet myRs = null;

            try {
                // 1. Get a connection to database
                myConn = ConnectionProvider.getcon();

                // 2. Create a statement
                myStmt = myConn.createStatement();

                // 3. Execute SQL query
                myRs = myStmt.executeQuery("select * from products where name = '" + name + "'");

                while (myRs.next()) {
                    CheckName = 1;
                    Iconlabel1.setIcon(new ImageIcon("src\\images\\no.png"));
                    Iconlabel1.setText("");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else {
            Iconlabel1.setVisible(false);
        }
    }//GEN-LAST:event_nametxtKeyReleased

