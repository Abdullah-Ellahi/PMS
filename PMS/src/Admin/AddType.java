    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        String type = typenametxt.getText();

        Connection con, c;
        PreparedStatement stm;
        Statement st;
        int a = 0;

        try {
            c = ConnectionProvider.getcon();
            st = c.createStatement();
            ResultSet rs = st.executeQuery("Select * From types");

            while (rs.next()) {
                if (rs.getString("type_name").equals(type)) {
                    a = 1;
                }
            }

            c.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        if (type.equals("")) {
            JOptionPane.showMessageDialog(null, "type name cannot be empty!");
        } else if (CheckName == 1) {
            JOptionPane.showMessageDialog(null, "Product Type already exists!\nPlease enter another type name.");
        } else if (a == 1) {
            JOptionPane.showMessageDialog(null, "Product Type already exists!\nPlease enter another type name.");
        } else {

            try {
                con = ConnectionProvider.getcon();

                stm = con.prepareStatement("insert into types (type_name) values(?)");

                stm.setString(1, type);

                stm.executeUpdate();

                JOptionPane.showMessageDialog(null, "Type added successfully!");

                jTable1ComponentShown();
                jButton17ActionPerformed(evt);

            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

