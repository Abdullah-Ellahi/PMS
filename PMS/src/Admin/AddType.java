    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Please select a record to edit!");
        } else {
            String id = (String) model.getValueAt(index, 0);
            String Otype_name = (String) model.getValueAt(index, 1);
            String Ntype_name = typenametxt.getText();

            if (Ntype_name.equals("")) {
                JOptionPane.showMessageDialog(null, "Type name cannot be empty!");
            } else if (CheckName == 1 && !Ntype_name.equals(Otype_name) && !(Ntype_name.toUpperCase()).equals(Otype_name.toUpperCase())
                && !(Ntype_name.toLowerCase()).equals(Otype_name.toLowerCase())) {

                JOptionPane.showMessageDialog(null, "Product type already exists!\nPlease enter another type.");
            } else {

                Connection con;
                PreparedStatement stm;

                try {
                    con = ConnectionProvider.getcon();
                    stm = con.prepareStatement("UPDATE `pharmacy`.`types` SET `type_name` = ? WHERE (`id` = ?) and (`type_name` = ?);");

                    stm.setString(1, Ntype_name);
                    stm.setString(2, id);
                    stm.setString(3, Otype_name);

                    stm.executeUpdate();

                    stm = con.prepareStatement("UPDATE `pharmacy`.`products` SET `type` = ? WHERE (`type` = ?);");

                    stm.setString(1, Ntype_name);
                    stm.setString(2, Otype_name);

                    stm.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Changes Saved successfully!");

                    //refreshing the table
                    jTable1ComponentShown();
                    jButton17ActionPerformed(evt);

                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

