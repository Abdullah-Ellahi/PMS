    private void typenametxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_typenametxtKeyReleased
        // TODO add your handling code here:
        String name = typenametxt.getText();

        if (!name.equals("")) {
            CheckName = 0;

            Connection myConn = null;
            Statement myStmt = null;
            ResultSet myRs = null;

            try {
                // 1. Get a connection to database
                myConn = ConnectionProvider.getcon();

                // 2. Create a statement
                myStmt = myConn.createStatement();

                // 3. Execute SQL query
                myRs = myStmt.executeQuery("select * from types where type_name = '" + name + "'");

                while (myRs.next()) {
                    if (name.equals(myRs.getString("type_name"))) {
                        CheckName = 0;
                    }
                    CheckName = 1;
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_typenametxtKeyReleased

