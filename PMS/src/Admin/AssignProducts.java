    private void Distributors() {
        Connection con;
        Statement stm;
        ResultSet rs;
        try {
            con = ConnectionProvider.getcon();
            stm = con.createStatement();
            rs = stm.executeQuery("select * from distributors");

            while (rs.next()) {
                distributorcombotxt.addItem(rs.getString("company_name"));
            }
            distributorcombotxt.setSelectedItem("Not Specified");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

