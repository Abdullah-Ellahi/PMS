    private void jTable1ComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_jTable1ComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        try {
            for (int i = jTable1.getRowCount() - 1; i >= 0; i--) {
                model.removeRow(i);
            }

            Connection con = ConnectionProvider.getcon();
            Statement stm, st = con.createStatement();
            ResultSet rst, rs = st.executeQuery("Select * From distributors");

            int i = 1;

            while (rs.next()) {

                stm = con.createStatement();
                rst = stm.executeQuery("Select * From products WHERE distributor_company = '" + rs.getString("company_name") + "'");

                while (rst.next()) {
                    model.addRow(new Object[]{i, rst.getString("name"), rs.getString("company_name"), rs.getString("supplier")});
                    i++;
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTable1ComponentShown

